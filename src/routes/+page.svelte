    <h1 class="text-3xl mb-6 font-bold text-amber-900">
        De Kempen zijn geen provincie.
    </h1>
    <p>
        Het is een onherroepelijk feit: die miserabele zandstreek in Antwerpen
        en Limburg is geen provincie. Ze hebben geen aparte provinciale klasse,
        geen provinciaal wapenschild en je kunt er niet voor zuipen op een
        zichzelf respecterende provinciezuip. Het woord "Kempenaar" duidt zowel
        op een bewoner van dat misselijke gebied (en vaak zijn het ook
        chauvinistische sujetten) als op een bepaald soort aardappel: dat zegt
        genoeg, dunkt mij.
    </p>
